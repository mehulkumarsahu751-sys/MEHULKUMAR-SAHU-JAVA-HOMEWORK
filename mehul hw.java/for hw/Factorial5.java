// Factorial of 5

public class Factorial5 {
    public static void main(String[] args) {
        int fact = 1;
        int n = 5;

        for(int i = 1; i <= n; i++) {
            fact *= i;
        }

        System.out.println("Factorial: " + fact);
    }
}


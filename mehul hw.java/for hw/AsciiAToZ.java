// ASCII of a–z

public class AsciiAToZ {
    public static void main(String[] args) {
        for(char c = 'a'; c <= 'z'; c++) {
            System.out.println(c + " = " + (int)c);
        }
    }
}

// Output: a = 97, b = 98, c = 99, ... z = 122

